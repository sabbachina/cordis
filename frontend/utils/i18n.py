def t(key: str, **kwargs) -> str:
    """
    Return the translated string for *key* in the active language.
    Falls back to Italian, then to the key itself.
    Supports Python str.format()-style keyword substitutions via **kwargs.
    """
    lang = st.session_state.get("lang", "it")
    text = TRANSLATIONS.get(lang, TRANSLATIONS["it"]).get(key)
    if text is None:
        text = TRANSLATIONS["it"].get(key, key)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text


def render_language_selector() -> None:
    """
    Render a language radio button in the Streamlit sidebar.
    Persists the chosen language in st.session_state.lang.
    """
    if "lang" not in st.session_state:
        st.session_state.lang = "it"

    langs = {"🇮🇹 Italiano": "it", "🇬🇧 English": "en"}
    current_label = next((k for k, v in langs.items() if v == st.session_state.lang), "🇮🇹 Italiano")

    choice = st.sidebar.radio(
        t("language_label"),
        list(langs.keys()),
        index=list(langs.keys()).index(current_label),
        key="lang_radio",
        horizontal=True,
    )
    st.session_state.lang = langs[choice]
